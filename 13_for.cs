// Los bucles for son utilizados para repetir un bloque de código un número determinado de veces.
using System;

namespace input
{
    class Program
    {
        public static void Main(string[] args)
        {
            // Ciclo for
            for (int i = 0; i < 5; i++)
            {
                Console.WriteLine($"Valor de i: {i}");
            }
        }
    }
}